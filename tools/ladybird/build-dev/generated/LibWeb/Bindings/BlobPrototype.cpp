void BlobPrototype::initialize(JS::Realm& realm)
{


    [[maybe_unused]] auto& vm = realm.vm();


    [[maybe_unused]] u8 default_attributes = JS::Attribute::Enumerable | JS::Attribute::Configurable | JS::Attribute::Writable;


    set_prototype(realm.intrinsics().object_prototype());


    auto native_size_getter = JS::NativeFunction::create(realm, size_getter, 0, "size"_utf16_fly_string, &realm, "get"sv);

    GC::Ptr<JS::NativeFunction> native_size_setter;

    define_direct_accessor("size"_utf16_fly_string, native_size_getter, native_size_setter, default_attributes);

    auto native_type_getter = JS::NativeFunction::create(realm, type_getter, 0, "type"_utf16_fly_string, &realm, "get"sv);

    GC::Ptr<JS::NativeFunction> native_type_setter;

    define_direct_accessor("type"_utf16_fly_string, native_type_getter, native_type_setter, default_attributes);

    define_native_function(realm, "slice"_utf16_fly_string, slice, 0, default_attributes);

    define_native_function(realm, "stream"_utf16_fly_string, stream, 0, default_attributes);

    define_native_function(realm, "text"_utf16_fly_string, text, 0, default_attributes);

    define_native_function(realm, "arrayBuffer"_utf16_fly_string, array_buffer, 0, default_attributes);

    define_native_function(realm, "bytes"_utf16_fly_string, bytes, 0, default_attributes);

    define_direct_property(vm.well_known_symbol_to_string_tag(), JS::PrimitiveString::create(vm, "Blob"_string), JS::Attribute::Configurable);

    Base::initialize(realm);

}

