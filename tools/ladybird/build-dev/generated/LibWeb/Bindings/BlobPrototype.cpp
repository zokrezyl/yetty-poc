JS_DEFINE_NATIVE_FUNCTION(BlobPrototype::slice)
{
    WebIDL::log_trace(vm, "BlobPrototype::slice");
    [[maybe_unused]] auto& realm = *vm.current_realm();

    auto* impl = TRY(impl_from(vm));

    auto arg0 = vm.argument(0);

    Optional<WebIDL::LongLong> start;

    if (!arg0.is_undefined())

    start = TRY(WebIDL::convert_to_int<WebIDL::LongLong>(vm, arg0, WebIDL::EnforceRange::No, WebIDL::Clamp::Yes));

    auto arg1 = vm.argument(1);

    Optional<WebIDL::LongLong> end;

    if (!arg1.is_undefined())

    end = TRY(WebIDL::convert_to_int<WebIDL::LongLong>(vm, arg1, WebIDL::EnforceRange::No, WebIDL::Clamp::Yes));

    auto arg2 = vm.argument(2);

    Optional<String> content_type;

    if (!arg2.is_undefined()) {
        if (!false || !arg2.is_null())
            content_type = TRY(WebIDL::to_string(vm, arg2));
    }

    [[maybe_unused]] auto retval = TRY(throw_dom_exception_if_needed(vm, [&] { return impl->slice(start, end, content_type); }));

    return &const_cast<Blob&>(*retval);

}

