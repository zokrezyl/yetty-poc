GC_DEFINE_ALLOCATOR(NavigatorConstructor);

NavigatorConstructor::NavigatorConstructor(JS::Realm& realm)
    : NativeFunction("Navigator"_utf16_fly_string, realm.intrinsics().function_prototype())
{
}

NavigatorConstructor::~NavigatorConstructor()
{
}

JS::ThrowCompletionOr<JS::Value> NavigatorConstructor::call()
{
    return vm().throw_completion<JS::TypeError>(JS::ErrorType::ConstructorWithoutNew, "Navigator");
}


JS::ThrowCompletionOr<GC::Ref<JS::Object>> NavigatorConstructor::construct([[maybe_unused]] FunctionObject& new_target)
{
    WebIDL::log_trace(vm(), "NavigatorConstructor::construct");

    return vm().throw_completion<JS::TypeError>(JS::ErrorType::NotAConstructor, "Navigator");
}


void NavigatorConstructor::initialize(JS::Realm& realm)
{
    auto& vm = this->vm();
    [[maybe_unused]] u8 default_attributes = JS::Attribute::Enumerable;

    Base::initialize(realm);

    define_direct_property(vm.names.length, JS::Value(0), JS::Attribute::Configurable);
    define_direct_property(vm.names.name, JS::PrimitiveString::create(vm, "Navigator"_string), JS::Attribute::Configurable);
    define_direct_property(vm.names.prototype, &ensure_web_prototype<NavigatorPrototype>(realm, "Navigator"_fly_string), 0);


}

