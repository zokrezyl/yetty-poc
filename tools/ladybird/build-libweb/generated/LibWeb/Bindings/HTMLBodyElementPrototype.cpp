[[maybe_unused]] static JS::ThrowCompletionOr<HTML::HTMLBodyElement*> impl_from(JS::VM& vm)
{
    auto this_value = vm.this_value();
    JS::Object* this_object = nullptr;
    if (this_value.is_nullish())
        this_object = &vm.current_realm()->global_object();
    else
        this_object = TRY(this_value.to_object(vm));

    if (!is<HTML::HTMLBodyElement>(this_object))
        return vm.throw_completion<JS::TypeError>(JS::ErrorType::NotAnObjectOfType, "HTMLBodyElement");
    return static_cast<HTML::HTMLBodyElement*>(this_object);
}

